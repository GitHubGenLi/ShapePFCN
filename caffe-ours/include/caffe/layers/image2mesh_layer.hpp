#ifndef CAFFE_IMAGE2MESH_LAYER_HPP_
#define CAFFE_IMAGE2MESH_LAYER_HPP_

#include <vector>

#include "caffe/blob.hpp"
#include "caffe/layer.hpp"
#include "caffe/proto/caffe.pb.h"

namespace caffe {

template <typename Dtype>
class Image2MeshLayer : public Layer<Dtype> {
 public:
  explicit Image2MeshLayer(const LayerParameter& param)
      : Layer<Dtype>(param) {}
  virtual void LayerSetUp(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top);
  virtual void Reshape(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top);

  virtual inline const char* type() const { return "Image2Mesh"; }
  virtual inline int MinBottomBlobs() const { return 4; } // triangle id blob + labels + at least 2 views
  virtual inline int ExactNumTopBlobs() const { return 1; }

 protected:
  virtual void Forward_cpu(const vector<Blob<Dtype>*>& bottom,
      const vector<Blob<Dtype>*>& top);
  virtual void Backward_cpu(const vector<Blob<Dtype>*>& top,
      const vector<bool>& propagate_down, const vector<Blob<Dtype>*>& bottom);

  Image2MeshParameter_Image2MeshOp op_;
  Blob<int> max_pixel_idx_; // stores which pixel in the input maps yielded the max per triangle           
  Blob<int> max_view_idx_; // stores which view in the input maps yielded the max per triangle      
  int max_num_faces_;
  int num_labels_;
  int num_pixels_;
};

}  // namespace caffe

#endif  // CAFFE_IMAGE2MESH_LAYER_HPP_
